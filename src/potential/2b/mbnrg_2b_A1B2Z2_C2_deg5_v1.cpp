double mbnrg_A1B2Z2_C2_deg5_v1::eval(const double* xyz1, const double* xyz2, const size_t n) {
    std::vector<double> energies(n, 0.0);
    std::vector<double> energies_sw(n, 0.0);

    std::vector<double> xyz(15);
    double sw = 0.0;
    polynomial my_poly;

    for (size_t j = 0; j < n; j++) {
        const double* mon1 = xyz1 + 9 * j;
        const double* mon2 = xyz2 + 6 * j;

        const double d12[3] = {mon1[0] - mon2[0], mon1[1] - mon2[1], mon1[2] - mon2[2]};

        const double d12rsq = d12[0] * d12[0] + d12[1] * d12[1] + d12[2] * d12[2];
        const double d12r = std::sqrt(d12rsq);

        if (true && d12r > m_ro) {
            continue;
        }

        std::copy(mon1, mon1 + 9, xyz.begin() + 0);

        std::copy(mon2, mon2 + 6, xyz.begin() + 9);

        const double* coords_A_1_a = xyz.data() + 0;

        const double* coords_B_1_a = xyz.data() + 3;

        const double* coords_B_2_a = xyz.data() + 6;

        const double* coords_C_1_b = xyz.data() + 9;

        const double* coords_C_2_b = xyz.data() + 12;

        double coords_Z_1_a[3];
        std::fill(coords_Z_1_a, coords_Z_1_a + 3, 0.0);

        double coords_Z_2_a[3];
        std::fill(coords_Z_2_a, coords_Z_2_a + 3, 0.0);

        double w12 = -9.721486914088159e-02;  // from MBpol
        double w13 = -9.721486914088159e-02;
        double wcross = 9.859272078406150e-02;

        monomer m1;
        m1.setup(coords_A_1_a, w12, wcross, coords_Z_1_a, coords_Z_2_a);

        variable vs[14];

        double xs[14];

        xs[0] = vs[0].v_exp(m_k_x_intra_A_B_1, coords_A_1_a, coords_B_1_a);
        xs[1] = vs[1].v_exp(m_k_x_intra_A_B_1, coords_A_1_a, coords_B_2_a);
        xs[2] = vs[2].v_exp(m_k_x_inter_A_C_0, coords_A_1_a, coords_C_1_b);
        xs[3] = vs[3].v_exp(m_k_x_inter_A_C_0, coords_A_1_a, coords_C_2_b);
        xs[4] = vs[4].v_exp(m_k_x_intra_B_B_1, coords_B_1_a, coords_B_2_a);
        xs[5] = vs[5].v_exp(m_k_x_inter_B_C_0, coords_B_1_a, coords_C_1_b);
        xs[6] = vs[6].v_exp(m_k_x_inter_B_C_0, coords_B_1_a, coords_C_2_b);
        xs[7] = vs[7].v_exp(m_k_x_inter_B_C_0, coords_B_2_a, coords_C_1_b);
        xs[8] = vs[8].v_exp(m_k_x_inter_B_C_0, coords_B_2_a, coords_C_2_b);
        xs[9] = vs[9].v_exp(m_k_x_intra_C_C_1, coords_C_1_b, coords_C_2_b);
        xs[10] = vs[10].v_coul(m_k_x_inter_C_Z_0, coords_C_1_b, coords_Z_1_a);
        xs[11] = vs[11].v_coul(m_k_x_inter_C_Z_0, coords_C_1_b, coords_Z_2_a);
        xs[12] = vs[12].v_coul(m_k_x_inter_C_Z_0, coords_C_2_b, coords_Z_1_a);
        xs[13] = vs[13].v_coul(m_k_x_inter_C_Z_0, coords_C_2_b, coords_Z_2_a);

        double gsw12 = 0.0;
        double sw12 = f_switch(d12r, gsw12);

        sw = sw12;

        energies[j] = my_poly.eval(xs, coefficients.data());
        energies_sw[j] = energies[j] * sw;
    }

    double energy = 0.0;
    for (size_t i = 0; i < n; i++) energy += energies_sw[i];

    return energy;
}

